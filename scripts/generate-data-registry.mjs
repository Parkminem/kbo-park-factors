import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const outputPath = path.join(root, "app", "generated-data.ts");

function loadJsonFiles(relativeDir, prefix) {
  const dir = path.join(root, relativeDir);
  if (!fs.existsSync(dir)) return { imports: [], entries: [] };

  const files = fs
    .readdirSync(dir)
    .filter((fileName) => fileName.endsWith(".json"))
    .sort();

  return files.reduce(
    (acc, fileName, index) => {
      const name = `${prefix}${index}`;
      const date = fileName.replace(/\.json$/, "");
      const importPath = `../${relativeDir}/${fileName}`;
      acc.imports.push(`import ${name} from "${importPath}";`);
      acc.entries.push(`  ${JSON.stringify(date)}: ${name},`);
      return acc;
    },
    { imports: [], entries: [] },
  );
}

const daily = loadJsonFiles("data/daily-factors", "dailyArtifact");
const validations = loadJsonFiles("data/validations", "validationArtifact");

const source = [
  "// This file is generated by scripts/generate-data-registry.mjs.",
  "// Do not edit it by hand.",
  ...daily.imports,
  ...validations.imports,
  "",
  "export const dailyArtifacts = {",
  ...daily.entries,
  "} as const;",
  "",
  "export const validationArtifacts = {",
  ...validations.entries,
  "} as const;",
  "",
].join("\n");

fs.writeFileSync(outputPath, source);
